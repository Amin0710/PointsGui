

public class Point {

	// variable declaration
	float x;
	float y;

	//creating the getter and setter for declared variables
	public float getX() {
		return x;
	}

	public void setX(float x) {
		this.x = x;
	}

	public float getY() {
		return y;
	}

	public void setY(float y) {
		this.y = y;
	}

}
